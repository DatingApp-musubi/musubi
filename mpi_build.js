const fs = require('fs');
const { Document, Packer, Paragraph, TextRun, Table, TableRow, TableCell,
        AlignmentType, LevelFormat, HeadingLevel, BorderStyle, WidthType,
        ShadingType, PageBreak, Header, Footer, PageNumber } = require('docx');

const F = "Malgun Gothic";
const CW = 9026; // A4 content width
const border = { style: BorderStyle.SINGLE, size: 1, color: "CCCCCC" };
const borders = { top: border, bottom: border, left: border, right: border };
const margins = { top: 60, bottom: 60, left: 120, right: 120 };

const P = (text, opts = {}) => new Paragraph({
  spacing: { after: opts.after ?? 120 },
  ...(opts.bullet ? { numbering: { reference: "bullets", level: 0 } } : {}),
  children: [new TextRun({ text, font: F, size: opts.size ?? 21, bold: opts.bold ?? false, color: opts.color })]
});
const H1 = t => new Paragraph({ heading: HeadingLevel.HEADING_1, children: [new TextRun({ text: t, font: F })] });
const H2 = t => new Paragraph({ heading: HeadingLevel.HEADING_2, children: [new TextRun({ text: t, font: F })] });
const PB = () => new Paragraph({ children: [new PageBreak()] });

function cell(text, w, opts = {}) {
  return new TableCell({
    borders, width: { size: w, type: WidthType.DXA }, margins,
    ...(opts.fill ? { shading: { fill: opts.fill, type: ShadingType.CLEAR } } : {}),
    children: [new Paragraph({
      alignment: opts.center ? AlignmentType.CENTER : AlignmentType.LEFT,
      children: [new TextRun({ text, font: F, size: 20, bold: opts.bold ?? false })]
    })]
  });
}

// item table: [no, item text, R?]
function itemTable(items, prefix) {
  const cols = [800, 7326, 900];
  const rows = [new TableRow({ children: [
    cell("번호", cols[0], { fill: "EFE6D5", bold: true, center: true }),
    cell("문항", cols[1], { fill: "EFE6D5", bold: true }),
    cell("채점", cols[2], { fill: "EFE6D5", bold: true, center: true })
  ] })];
  items.forEach((it, i) => {
    rows.push(new TableRow({ children: [
      cell(`${prefix}-${i + 1}`, cols[0], { center: true }),
      cell(it[0], cols[1]),
      cell(it[1] ? "역(R)" : "정", cols[2], { center: true })
    ] }));
  });
  return new Table({ width: { size: CW, type: WidthType.DXA }, columnWidths: cols, rows });
}

function scaleBlock(code, name, def, cut, items) {
  return [
    H2(`${code}. ${name} (8문항)`),
    P(`구성개념: ${def}`),
    P(`예비 커트라인: ${cut}`, { color: "8a6d2f" }),
    itemTable(items, code),
    P("", { after: 160 })
  ];
}

